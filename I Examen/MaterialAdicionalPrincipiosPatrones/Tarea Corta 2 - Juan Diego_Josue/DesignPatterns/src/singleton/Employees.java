package singleton;

/**
 *
 * @author Juan
 */
public class Employees {
    private String name;
    private int age;
    
    public Employees(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    @Override
    public String toString() {
        return "Nombre: " + name + ", edad: " + age;
    }
    
}
